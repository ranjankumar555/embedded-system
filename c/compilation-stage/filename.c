#include<stdio.h>
#define PI 3.14		// macro
int main(){
	int i = 5;      // variable of type 'int'
	char ch = 'A';  // variable of type 'char'
	float f = 23.5f; // variable of type 'float'

	// printing the data
	printf("i = %d\n", i);
	printf("ch = %c\n",ch);
	printf("f = %f\n", f);

	printf("pi = %d", PI);

	return 0;
}
